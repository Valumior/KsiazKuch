    private void loadPrzepisData(Przepis przepis) {
        this.przepisyPrzyrzadzenieTextEdit.setText(przepis.getInstrukcja());
        this.przepisNazwaLabel.setText(przepis.getNazwa());
        this.przepisCzasPrzygotowaniaLabel.setText(Integer.toString(przepis.getCzasPrzygotowania()) + " minut");

        switch(przepis.getTrudnosc()) {
            case Latwe:
                this.przepisTrudnosclabel.setText("ŁATWE");
                break;
            case Srednie:
                this.przepisTrudnosclabel.setText("ŚREDNIE");
                break;
            case Trudne:
                this.przepisTrudnosclabel.setText("TRUDNE");
                break;
            case MistrzKuchni:
                this.przepisTrudnosclabel.setText("MISTRZ KUCHNI");
                break;
            default:
                break;
        }

        double kalorieTotal = 0, tluszcze_calkowiteTotal = 0, tluszcze_nasyconeTotal = 0, tluszcze_nienasyconeTotal = 0,
            cholesterolTotal = 0, sodTotal = 0, weglowodanyTotal = 0, blonnikTotal = 0, cukryTotal = 0, bialkaTotal = 0;

        ArrayList<String> skladniki = new ArrayList<>();
        
        for(PrzepisSkladnik skladnik : przepis.getSkladniki()) {
            double ratio = 1;
            if(skladnik.getSkladnik().getMiara() == Skladnik.Miara.Mililitry || skladnik.getSkladnik().getMiara() == Skladnik.Miara.Gramy) {
                ratio = 100;
            }

            if(skladnik.getSkladnik().getMiara() == Skladnik.Miara.Mililitry)
                skladniki.add(skladnik.getSkladnik().getNazwa() + " " + Integer.toString(skladnik.getLiczba()) + "ml");
            else if(skladnik.getSkladnik().getMiara() == Skladnik.Miara.Gramy)
                skladniki.add(skladnik.getSkladnik().getNazwa() + " " + Integer.toString(skladnik.getLiczba()) + "g");
            else
                skladniki.add(skladnik.getSkladnik().getNazwa() + " " + Integer.toString(skladnik.getLiczba()) + " szt.");

            ratio = skladnik.getLiczba() / ratio;

            WartosciOdzywcze wartosciOdzywcze = skladnik.getSkladnik().getWartosciOdzywcze();
            kalorieTotal += wartosciOdzywcze.getKalorie() * ratio;
            tluszcze_calkowiteTotal += wartosciOdzywcze.getTluszczeCalkowite() * ratio;
            tluszcze_nasyconeTotal += wartosciOdzywcze.getTluszczeNasycone() * ratio;
            tluszcze_nienasyconeTotal += wartosciOdzywcze.getTluszczeNienasycone() * ratio;
            cholesterolTotal += wartosciOdzywcze.getCholesterol() * ratio;
            sodTotal += wartosciOdzywcze.getSod() * ratio;
            weglowodanyTotal += wartosciOdzywcze.getWeglowodany() * ratio;
            blonnikTotal += wartosciOdzywcze.getBlonnik() * ratio;
            cukryTotal += wartosciOdzywcze.getCukry() * ratio;
            bialkaTotal += wartosciOdzywcze.getBialka() * ratio;
        }
        
        this.przepisSkladnikiList.setListData(skladniki.toArray());

        this.przepisKalorieLabel.setText(Double.toString(kalorieTotal));
        this.przepisTluszczeCalkowiteLabel.setText(Double.toString(tluszcze_calkowiteTotal));
        this.przepisTluszczeNasyconeLabel.setText(Double.toString(tluszcze_nasyconeTotal));
        this.przepisTluszczeNienasyconeLabel.setText(Double.toString(tluszcze_nienasyconeTotal));
        this.przepisCholesterolLabel.setText(Double.toString(cholesterolTotal));
        this.przepisSodLabel.setText(Double.toString(sodTotal));
        this.przepisWeglowodanyLabel.setText(Double.toString(weglowodanyTotal));
        this.przepisBlonnikLabel.setText(Double.toString(blonnikTotal));
        this.przepisCukryLabel.setText(Double.toString(cukryTotal));
        this.przepisBialkaLabel.setText(Double.toString(bialkaTotal));
    }

